los Fondos de Desarrollo Local para socializar y hacerle seguimiento a la ejecución.
La UMV se comprometió a mejorar el estado de la mayor cantidad de bicicarriles con los que cuenta la capital, 
razón por la que se han realizado más de 390 visitas técnicas y se han priorizado 122 segmentos de los cuales se han atendido 103, 95 terminados y 8 más en ejecución.

Para este año la Secretaría de Movilidad,  
tiene contemplado implementar 18 nuevas rutas de bicicarriles en la ciudad y la UMV será la encargada de adecuar estos segmentos viales para su ejecución.

